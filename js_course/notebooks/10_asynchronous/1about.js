/*

Asynchronous operations are those that do not block the execution of the program while they are running.
There is no need to wait for one async operation to finish before starting the next one.
Asynchronous is not the same as concurrent or multi-threaded, it only means non-blocking.
Recall that JavaScript is single-threaded.

There are three patterns:

1. Callbacks.
2. Promises.
3. Async/Await.

Examples of async operations: network requests, I/O operations.
*/
